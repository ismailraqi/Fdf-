#include "fdf.h"

void	ft_putstr(char *s)
{
	size_t	i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		write(1, &s[i], sizeof(char));
		i++;
	}
}
